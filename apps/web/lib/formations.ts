/**
 * Возвращает координаты для формации, фолбэк к 4-3-3 если неизвестная.
 */
export function getFormationCoords(key?: string | null): Coord[] {
  if (!key) return FORMATION_COORDS["4-3-3"];
  return (
    FORMATION_COORDS[key as FormationKey] ?? FORMATION_COORDS["4-3-3"]
  );
}

const LIGHT_TOKENS = new Set([
  "#ffffff",
  "#FFFFFF",
  "#eab308",
  "#f97316",
  "#fbbf24",
  "#7e96d8",
]);

/**
 * Цвет текста (номер) на фишке игрока — тёмный на светлых, белый на тёмных.
 */
export function tokenTextColor(bg?: string): string {
  if (!bg) return "white";
  return LIGHT_TOKENS.has(bg.toLowerCase()) ? "#1e293b" : "white";
}

/**
 * Цвет обводки фишки — против белого фона делаем тёмную обводку, иначе светлую.
 */
export function tokenStroke(bg?: string): string {
  if (!bg) return "rgba(255,255,255,0.9)";
  return bg.toLowerCase() === "#ffffff"
    ? "rgba(0,0,0,0.15)"
    : "rgba(255,255,255,0.9)";
}
